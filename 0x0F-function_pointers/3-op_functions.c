#include "3-calc.h"

/**
 * op_add - add
 * @a: first
 * @b: second
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub
 * @a: first
 * @b: second
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multible
 * @a: first
 * @b: second
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: first
 * @b: second
 * Return: a * b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - a % b
 * @a: first
 * @b: second
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
